#!/bin/bash

# Exit on error
set -e

echo "Running collectstatic..."
python manage.py collectstatic --noinput --clear

echo "Starting gunicorn..."
exec gunicorn bfg.wsgi:application \
    --bind 0.0.0.0:${PORT:-8000} \
    --workers 3 \
    --timeout 120 \
    --access-logfile - \
    --error-logfile -
